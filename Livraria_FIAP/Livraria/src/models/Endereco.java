package models;

public class Endereco {

    private String rua;
    private int numero;
    private String cep;
    private String cidade;
    private String endereco;

    public Endereco(String rua, int numero, String cep, String cidade, String endereco){
        this.rua = rua;
        this. numero = numero;
        this.cep = cep;
        this.cidade = cidade;
        this.endereco = endereco;
    }




}
